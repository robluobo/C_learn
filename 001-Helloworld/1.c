//第一个程序，Hello world 
#include <stdio.h>  //编译预处理指令，stdio.h是系统提供的文件名，.h后缀表示为头文件，//是注释 
int main()          //定义主函数,int表示为整型，main是函数名，为主函数，每个c程序都必须有一个主函数 
{
	printf("Hello World!\n");   //printf语句,\n是换行 ，内容用双引号括起来 
	return 0;               //当mian函数执行结束前将整数0作为函数数值，返回到调用函数处（有时取值有意义）    
}
/*每次完成一项命令，最后要用分号*/

 
 










































































































































